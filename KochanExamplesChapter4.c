// Question 4
//
// The factorial of an integer n, the written n!, is the product of the consecutive integers 1 through n. For example,
// 5 factorial is calculated as:
// 
// 5! = 5 x 4 x 3 x 2 x 1 = 120
// 
// Write a program to generate and print a table of the first 10 factorials. 
// 

#include <stdio.h>

int main(int argc, const char * argv[]) {
    
    int n;
    int f = 1;
    printf("n  |  n!\n");
    printf("--------\n");
    for(n=1; n<11; ++n) {
        f = f*n;
        printf("%i  |  %i\n", n, f);
    }
    return 0;
}



// Question 4
//
// Write a program that converts X degrees fahrenheit (f) to degrees (c) using the following formula: 
// c = (f - 32) / 1.8
//

#include <stdio.h>

int main(int argc, const char * argv[]) {
    float c, f;
    printf("Enter the temperature in fahrenheit.\n");
    scanf("%f", &f);
    c = (f - 32) / 1.8;
    printf("%f degrees fahrenheit is equal to %f degrees celsius.\n", f, c);
    return 0;
}


// Question 11
//
// Write a program that calculates the sum of the digits in an integer. For example, the sum of the digits in the 
// number 2155 is 2 + 1 + 5 + 5 or 13. The program should accept any arbitrary integer typed in by the user. 
// 
// Hint: This can be done with a while loop. 
// 


#include <stdio.h>

int main(int argc, const char * argv[]) {
    
    int n, remainder, sum = 0;
    
    printf("Enter your number:\n");
    scanf("%i", &n);
    
    while (n != 0) {
        remainder = n % 10;
        sum = sum + remainder;
        n = n / 10;
    }
    printf("%i\n", sum);

    
    return 0;
}
 